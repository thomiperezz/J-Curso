package operadores;

public class OperadoresCombinados {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("a = " + a);

        a+=5; // operador combinado de suma
        System.out.println("a = " + a);

        a-=5; // operador combinado de resta
        System.out.println("a = " + a);

        System.out.println("---------------------------");
        a/=10; // operador combinado de la división
        System.out.println("a = " + a);

        a*=100; // operador combinado de la multiplicación
        System.out.println("a = " + a);

        System.out.println("---------------------------");
    }
}
